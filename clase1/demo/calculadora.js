const calculadora = {
    sumar: (valorA, valorB) =>{
        return valorA + valorB;
    },
    restar:(valorA, valorB) => {
        return valorA - valorB;
    },
    multi:(valorA, valorB) => {
        return valorA * valorB;
    },
    div:(valorA, valorB) => {
        return valorA / valorB;
    }
}

